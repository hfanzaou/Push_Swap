#include "ft_printf.h"

void	ft_putnbr(int n, int *l)
{
	unsigned int	i;

	i = n;
	if (n < 0)
	{
		i = -n;
		ft_putchar('-', l);
	}
	if (i > 9)
	{
		ft_putnbr(i / 10, l);
	}
	ft_putchar(i % 10 + 48, l);
}
